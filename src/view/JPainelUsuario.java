    public JPainelUsuario(String usuarioLogado) {
        this.usuarioLogado = usuarioLogado;

        setTitle("    Painel do FuncionÃ¡rio     ");
        setSize(500, 250);
        setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        setLocationRelativeTo(null);
        setLayout(new FlowLayout());

        JButton btnAbrirChamados = new JButton("Criar Chamados");
        btnAbrirChamados.setPreferredSize(new Dimension(200, 70));
        btnAbrirChamados.addActionListener(e -> {
            new TelaChamadosUsuario(usuarioLogado).setVisible(true);
            dispose();
        });
        
        JButton btnAbrirAvisos = new JButton("Ver Avisos");
        btnAbrirAvisos.setPreferredSize(new Dimension(200, 70));        
        btnAbrirAvisos.addActionListener(e -> {     
            new TelaAvisos(usuarioLogado).setVisible(true);
            dispose();
    });
        add(btnAbrirChamados);
        add(btnAbrirAvisos);
    }

